package java.Setting;

public class KeySettings {
    public static String rightKey = "RIGHT";
    public static String downKey = "DOWN";
    public static String leftKey = "LEFT";
    public static String upKey = "UP";
    public static String spaceKey = "SPACE";

    public static String getRightKey() {
        return rightKey;
    }

    public static void setRightKey(String key) {
        rightKey = key;
    }

    public static String getDownKey() {
        return downKey;
    }

    public static void setDownKey(String key) {
        downKey = key;
    }

    public static String getLeftKey() {
        return leftKey;
    }

    public static void setLeftKey(String key) {
        leftKey = key;
    }

    public static String getUpKey() {
        return upKey;
    }

    public static void setUpKey(String key) {
        upKey = key;
    }

    public static String getSpaceKey() {
        return spaceKey;
    }

    public static void setSpaceKey(String key) {
        spaceKey = key;
    }
}